const EmailRegex =
  "([!#-'*+/-9=?A-Z^-~-]+(.[!#-'*+/-9=?A-Z^-~-]+)*|\"([]!#-[^-~ \t]|(\\[\t -~]))+\")@([!#-'*+/-9=?A-Z^-~-]+(.[!#-'*+/-9=?A-Z^-~-]+)*|[[\t -Z^-~]*])";

let regex = new RegExp(EmailRegex);

export function checkValidationEmail(checkEmail: string) {
  return regex.test(checkEmail);
}
